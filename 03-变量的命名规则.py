# 变量命名方式： 驼峰命名法 和  下划线命名法

# 驼峰命名法: 小驼峰和大驼峰

# 小驼峰: 第一个单词首字母要小写，其它单词首字母都大写
# 大驼峰: 每个单词首字母都大写

# 下划线命名： 单词都使用小写字母，单词之间使用下划线进行分割, 比如: my_name

my_name = '李四' # 下划线命名法 -> 推荐使用
myName = '王五' # 小驼峰
MyName = '赵六' # 大驼峰


